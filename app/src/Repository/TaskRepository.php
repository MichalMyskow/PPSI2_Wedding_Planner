    public function findAllCompleted(Wedding $wedding)
    {
        return $this->createQueryBuilder('g')
            ->andWhere('g.completed = :completed')
            ->setParameter('completed', true)
            ->andWhere('g.wedding = :wedding')
            ->setParameter('wedding', $wedding)
            ->getQuery()
            ->getResult();
    }

